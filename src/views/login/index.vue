<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" autocomplete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">
          {{ $t('wafTitle') }}
        </h3>
      <!-- <lang-select class="set-language" />-->
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model.trim="loginForm.username"
          :placeholder="$t('login.label.username')"
          name="username"
          type="text"
          tabindex="1"
          autocomplete="on"
        />
      </el-form-item>

      <el-tooltip v-model="capsTooltip" :content="$t('login.capsLock')" placement="right" manual>
        <el-form-item prop="password">
          <span class="svg-container">
            <svg-icon icon-class="password" />
          </span>
          <el-input
            :key="passwordType"
            ref="password"
            v-model.trim="loginForm.password"
            :type="passwordType"
            :placeholder="$t('login.label.password')"
            name="password"
            tabindex="2"
            autocomplete="on"
            @keyup.native="checkCapslock"
            @blur="capsTooltip = false"
            @keyup.enter.native="handleLogin"
          />
          <span class="show-pwd" @click="showPwd">
            <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
          </span>
        </el-form-item>
      </el-tooltip>

      <el-form-item v-if="show_totp_token" prop="totp_token">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          ref="totp_token"
          v-model.trim="loginForm.totp_token"
          :placeholder="$t('login.label.totp_token')"
          name="totp_token"
          type="text"
          tabindex="1"
          autocomplete="on"
        />
      </el-form-item>

      <el-button :loading="loading" type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">
        {{ $t('login.label.logIn') }}
      </el-button>

    </el-form>

  </div>
</template>

<script>
// import { validUsername } from '@/utils/validate'
// import LangSelect from '@/components/LangSelect'
// import { getAccessToken } from '@/api/login'

export default {
  name: 'Login',
  // components: { LangSelect },
  data() {
    return {
      show_totp_token: false,
      loginForm: {
        username: '',
        password: '',
        totp_token: '',
      },
      loginRules: {
        username: [{ required: true, message: '请输入账号', trigger: 'blur' }],
        password: [{ required: true, message: '请输入密码', trigger: 'blur' }]
      },
      passwordType: 'password',
      capsTooltip: false,
      loading: false,
      redirect: undefined,
      otherQuery: {}
    }
  },
  watch: {
    // 监听路由变化，提取路由中 redirect 参数
    // 用于在 token 过期重新登录后返回原页面
    $route: {
      handler: function(route) {
        const query = route.query
        if (query) {
          this.redirect = query.redirect
          this.otherQuery = this.getOtherQuery(query)
        }
      },
      immediate: true
    }
  },
  created() {

  },
  mounted() {
    if (this.loginForm.username === '') {
      this.$refs.username.focus()
    } else if (this.loginForm.password === '') {
      this.$refs.password.focus()
    }
  },
  destroyed() {

  },
  methods: {
    // 检查caps
    checkCapslock(e) {
      const { key } = e
      var keyCode = e.keyCode || e.which
      var isShift = e.shiftKey || (keyCode === 16) || false
      this.capsTooltip = key && key.length === 1 && (key >= 'A' && key <= 'Z') && !isShift
    },

    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },

    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('user/login', this.loginForm).then(() => {
            this.$router.push({ path: this.redirect || '/', query: this.otherQuery })
            this.loading = false
          }).catch((e) => {
            console.log(e)
            // if (e.response.data.errors.code === 50017) {
            //   this.show_totp_token = true
            // }
            this.loading = false
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },

    // 获取其他参数（args参数）
    getOtherQuery(query) {
      return Object.keys(query).reduce((acc, cur) => {
        if (cur !== 'redirect') {
          acc[cur] = query[cur]
        }
        return acc
      }, {})
    }
  }
}
</script>

